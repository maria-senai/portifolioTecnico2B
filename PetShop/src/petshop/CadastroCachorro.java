    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int matricula = Integer.parseInt(JOptionPane.showInputDialog("Qual a matrcula do cachorro que deseja remover?"));

        for (Cachorro c : cachorro) {
            if (c.getMatricula() == matricula) {
                cachorro.remove(c);
                JOptionPane.showMessageDialog(this, "Removendo o cachorro " + c.getNome());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

